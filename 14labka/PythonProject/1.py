from scipy.optimize import linprog

# –¶–µ–ª–µ–≤–∞—è —Ñ—É–Ω–∫—Ü–∏—è: –º–∞–∫—Å–∏–º–∏–∑–∏—Ä–æ–≤–∞—Ç—å 5x‚ÇÅ + 6x‚ÇÇ + 8x‚ÇÉ + x‚ÇÑ
# –î–ª—è linprog –Ω—É–∂–Ω–æ –º–∏–Ω–∏–º–∏–∑–∏—Ä–æ–≤–∞—Ç—å, –ø–æ—ç—Ç–æ–º—É –º–µ–Ω—è–µ–º –∑–Ω–∞–∫
c = [-5, -6, -8, -1]

# –û–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è (–ª–µ–≤–∞—è —á–∞—Å—Ç—å)
A = [
    [2, 1, 1, 1],   # 2x‚ÇÅ + x‚ÇÇ + x‚ÇÉ + x‚ÇÑ ‚â§ 4
    [2, 1, 2, 0],   # 2x‚ÇÅ + x‚ÇÇ + 2x‚ÇÉ ‚â§ 5
    [3, 2, 0, 0]    # 3x‚ÇÅ + 2x‚ÇÇ ‚â§ 5
]

# –ü—Ä–∞–≤–∞—è —á–∞—Å—Ç—å –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏–π
b = [4, 5, 5]

# –ì—Ä–∞–Ω–∏—Ü—ã –ø–µ—Ä–µ–º–µ–Ω–Ω—ã—Ö: x·µ¢ ‚â• 0
bounds = [(0, None)] * 4

# –†–µ—à–µ–Ω–∏–µ
res = linprog(c, A_ub=A, b_ub=b, bounds=bounds, method='highs')

# –í—ã–≤–æ–¥ —Ä–µ–∑—É–ª—å—Ç–∞—Ç–∞
if res.success:
    print("‚úÖ –û–ø—Ç–∏–º–∞–ª—å–Ω–æ–µ —Ä–µ—à–µ–Ω–∏–µ –Ω–∞–π–¥–µ–Ω–æ:")
    for i, val in enumerate(res.x, start=1):
        print(f"  x{i} = {val:.2f}")
    print(f"  –ú–∞–∫—Å–∏–º—É–º —Ü–µ–ª–µ–≤–æ–π —Ñ—É–Ω–∫—Ü–∏–∏ L = {(-res.fun):.2f}")
    print("\nüìò –û–±—ä—è—Å–Ω–µ–Ω–∏–µ:")
    print("  –ú–µ—Ç–æ–¥ –Ω–∞—à—ë–ª –æ–ø—Ç–∏–º–∞–ª—å–Ω–æ–µ —Ä–µ—à–µ–Ω–∏–µ, —É–¥–æ–≤–ª–µ—Ç–≤–æ—Ä—è—é—â–µ–µ –≤—Å–µ–º –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è–º.")
    print("  –ò—Å–ø–æ–ª—å–∑–æ–≤–∞–Ω —Å–∏–º–ø–ª–µ–∫—Å-–∞–ª–≥–æ—Ä–∏—Ç–º –±–µ–∑ –≥—Ä–∞—Ñ–∏–∫–∞.")
else:
    print("‚ùå –†–µ—à–µ–Ω–∏–µ –Ω–µ –Ω–∞–π–¥–µ–Ω–æ. –ü—Ä–æ–≤–µ—Ä—å –æ–≥—Ä–∞–Ω–∏—á–µ–Ω–∏—è.")
